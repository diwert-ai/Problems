    test_data = ([2, 1, 3, 2, 1],
                 [2, 4, 3, 5, 4, 2, 1, 3, 2, 1],
                 [1, 1, 2, 1, 1, 2, 1, 1, 2, 1, 1, 2, 1, 1, 2, 1, 1, 2, 1, 1])
    for costs in test_data:
        print(answer(costs))


